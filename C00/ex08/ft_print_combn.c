#include <stdio.h>

int	main(void)
{
	int m[3];

	m[2] = 0;
	while (m[0] <= 7)
	{
		m[1] = m[0] + 1;
		while (m[1] <= 8)
		{
			m[2] = m[1] + 1;
			while (m[2] <= 9)
			{
				printf("%d%d%d", m[0], m[1], m[2]);
				if (m[0] == 7 && m[1] == 8 && m[2] == 9)
					break ;
				printf(", ");
				m[2]++;
			}
			m[1]++;
		}
		m[0]++;
	}
}
